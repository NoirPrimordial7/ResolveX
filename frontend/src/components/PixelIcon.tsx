interface PixelIconProps extends Omit<SVGProps<SVGSVGElement>, "name"> {
  name: PixelIconName;
  size?: number;
  title?: string;
}

export default function PixelIcon({ className, name, size = 32, title, ...props }: PixelIconProps) {
  const rects = icons[name];

  return (
    <svg
      aria-hidden={title ? undefined : true}
      className={cn("inline-block shrink-0", className)}
      fill="currentColor"
      height={size}
      role={title ? "img" : undefined}
      shapeRendering="crispEdges"
      viewBox="0 0 32 32"
      width={size}
      {...props}
    >
      {title && <title>{title}</title>}
      {rects.map(([x, y, width, height], index) => (
        <rect height={height} key={`${name}-${index}`} width={width} x={x} y={y} />
      ))}
    </svg>
  );
}
